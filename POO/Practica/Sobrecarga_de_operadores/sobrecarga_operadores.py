# Sobrecarga de un operador: un operador puede comportarse de distintas formas.
# Sobreescritura tiene que ver con la herencia.
# Ej. operador "+"" se comporta de forma distinta, si estoy trabajando con tipo list,int or str.

a = 2
b = 3
print(a+b)

a = "Hola"
b = "Mundo"
print(a + b)

a = [1,2,3]
b = [4,5,6]
print(a + b)
