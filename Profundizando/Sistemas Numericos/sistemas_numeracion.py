# Decimal
a = 10
#print(f"a: {a}")
# Binario
a = 0b1010 # Asignal el mismo valor equivalente de binario a decimal 0b, para indicar que es un numero binario
#print(f"a: {a}")
# Octal
a = 0o12 #0o indicar que es un numero del sistema octal, 12 es el equivalente a 10 en el sistema octal
#print(f"a: {a}")
# Hexadecimal
a = 0xA #0x para indicar un valor en sistema hexadecimal, A es el equivalente a 10
#print(f"a: {a}")

# Conversion decimal
a = int('23', 10) # Por default, la base es decimal, da lo mismo si va 10 o no. Este es un numero ya no es un string
#print(f"a: {a}")

# Conversion binaria
a = int("10111", 2) # 2. Base binaria
